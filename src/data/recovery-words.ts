// -----------------------------------------------------------------------------
// carrot-code — Recovery phrase word bank
//
// Curated word lists for generating memorable 4-word recovery phrases.
// Pattern: [adjective] [animal] [verb] [noun]
//   e.g. "blue hamster loves reading"
//        "tiny dragon paints mountains"
//
// Design rules baked into these lists:
//   - K-2 reading level wherever possible (target: 6yo can read aloud).
//   - All words are pleasant / neutral. No words a parent would flinch
//     at. No violence, no money/value words, no negative emotions.
//   - All words are unambiguously spellable - if a 6yo hears the phrase
//     and tries to type it back, the spelling should be the one they
//     guess first.
//   - 100 words per slot = 100^4 = 100M combinations (~26 bits of
//     entropy). Plenty for "kid can't accidentally guess into someone
//     else's profile." Not adversarial-secure - not the threat model.
//   - Single-word only. No hyphens, no apostrophes, no spaces.
//   - All lowercase. Storage + comparison is case-insensitive on input.
//
// See:
//   src/services/profile-service.ts  - phrase generation + hashing
//   tests/unit/recovery-words.test.ts
// -----------------------------------------------------------------------------

